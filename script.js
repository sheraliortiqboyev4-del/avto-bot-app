// Initialize Telegram WebApp
const tg = window.Telegram.WebApp;
tg.expand();
tg.ready();

// State
let currentLang = 'uz';
let isDarkMode = true;

// DOM Elements
const loader = document.getElementById('loader');
const langBtn = document.getElementById('langBtn');
const langMenu = document.getElementById('langMenu');
const currentLangText = document.getElementById('currentLang');
const themeToggle = document.getElementById('themeToggle');
const sunIcon = document.getElementById('sunIcon');
const moonIcon = document.getElementById('moonIcon');

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Hide loader
    setTimeout(() => {
        loader.style.opacity = '0';
        setTimeout(() => loader.style.display = 'none', 500);
        startAnimations();
    }, 1500);

    // Lucide Icons
    lucide.createIcons();

    // Language Switcher
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langMenu.classList.toggle('show');
    });

    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', () => {
            const lang = option.getAttribute('data-lang');
            setLanguage(lang);
            langMenu.classList.remove('show');
        });
    });

    document.addEventListener('click', () => {
        langMenu.classList.remove('show');
    });

    // Theme Toggle
    themeToggle.addEventListener('click', () => {
        isDarkMode = !isDarkMode;
        document.body.classList.toggle('light-mode', !isDarkMode);
        document.body.classList.toggle('dark-mode', isDarkMode);
        sunIcon.classList.toggle('hidden', isDarkMode);
        moonIcon.classList.toggle('hidden', !isDarkMode);
        
        // Update TG colors if needed
        tg.setHeaderColor(isDarkMode ? '#05070a' : '#f0f2f5');
    });

    // Initial language setup
    setLanguage('uz');
    
    // Scroll Animations
    initScrollAnimations();
    
    // Stats Counter
    initCounters();
});

// Functions
function setLanguage(lang) {
    currentLang = lang;
    currentLangText.textContent = lang.toUpperCase();
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Update Buy Buttons Hrefs
    updateBuyLinks(lang);
}

function updateBuyLinks(lang) {
    const adminUsername = "AIiy_oka";
    document.querySelectorAll('.buy-btn').forEach(btn => {
        const planKey = `plan_${btn.getAttribute('data-plan')}`;
        const planName = translations[lang][planKey];
        const baseMsg = translations[lang]['buy_msg'];
        const fullMsg = baseMsg.replace('{plan}', planName);
        const encodedMsg = encodeURIComponent(fullMsg);
        btn.href = `https://t.me/${adminUsername}?text=${encodedMsg}`;
    });
}

function initScrollAnimations() {
    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.fade-in').forEach(el => {
        observer.observe(el);
    });
}

function initCounters() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const target = parseInt(entry.target.getAttribute('data-target'));
                animateCounter(entry.target, target);
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });

    document.querySelectorAll('.counter').forEach(counter => {
        observer.observe(counter);
    });
}

function animateCounter(el, target) {
    let current = 0;
    const duration = 2000;
    const stepTime = Math.abs(Math.floor(duration / (target / 100)));
    
    const timer = setInterval(() => {
        current += Math.ceil(target / 100);
        if (current >= target) {
            el.textContent = formatNumber(target) + '+';
            clearInterval(timer);
        } else {
            el.textContent = formatNumber(current) + '+';
        }
    }, stepTime);
}

function formatNumber(num) {
    if (num >= 1000000) return (num / 1000000).toFixed(1) + 'M';
    if (num >= 1000) return (num / 1000).toFixed(1) + 'K';
    return num;
}

